document.addEventListener("DOMContentLoaded", () => {
    Fread_mode();
  });
  
  function Fread_mode() {
    if (localStorage.getItem("data-night")) {
      $(".Fread_action_item.mode .icon-1").addClass("active");
      $(".Fread_action_item.mode .icon-2").removeClass("active");
      $.getScript(window.Fread.THEME_URL + "assets/js/Fread.bjxg.js");
    } else {
      $("html").removeAttr("data-night");
      $(".Fread_action_item.mode .icon-1").removeClass("active");
      $(".Fread_action_item.mode .icon-2").addClass("active");
    }
    
    $(".Fread_action_item.mode").on("click", () => {
      if (localStorage.getItem("data-night")) {
        $(".Fread_action_item.mode .icon-1").removeClass("active");
        $(".Fread_action_item.mode .icon-2").addClass("active");
        $("html").removeAttr("data-night");
        localStorage.removeItem("data-night");
      } else {
        $(".Fread_action_item.mode .icon-1").addClass("active");
        $(".Fread_action_item.mode .icon-2").removeClass("active");
        $("html").attr("data-night", "night");
        localStorage.setItem("data-night", "night");
        $.getScript(window.Fread.THEME_URL + "assets/js/Fread.bjxg.js");
      }
    });
    console.log('xmt 爱喝酸奶的.');
  }
  
  